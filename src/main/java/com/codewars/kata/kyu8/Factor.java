package com.codewars.kata.kyu8;

/**
 * @see <a href="https://www.codewars.com/kata/55cbc3586671f6aa070000fb">Grasshopper - Check for factor</a>
 */
class Factor
{
    static class Kata
    {
        static boolean checkForFactor(int base, int factor)
        {
            return base % factor == 0;
        }
    }
}
