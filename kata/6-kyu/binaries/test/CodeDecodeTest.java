import static org.junit.jupiter.api.Assertions.assertEquals;

import org.junit.jupiter.api.Test;

class CodeDecodeTest {
  @Test
  void encode() {
    assertEquals("0011100110", CodeDecode.code("62"));
    assertEquals("00111000011001", CodeDecode.code("69"));
    assertEquals("00011000001110", CodeDecode.code("86"));
    assertEquals("11101111110110110111", CodeDecode.code("10111213"));
    assertEquals("001101001101011101110011110011111010", CodeDecode.code("55337700"));
    assertEquals("1111110001100100110000110011000110010111011110101010001101001101", CodeDecode.code("1119441933000055"));
  }

  @Test
  void decode() {
    assertEquals("07", CodeDecode.decode("10001111"));
    assertEquals("33198877334422", CodeDecode.decode("01110111110001100100011000000110000011110011110111011100110000110001100110"));
    assertEquals("444666333666777444", CodeDecode.decode("001100001100001100001110001110001110011101110111001110001110001110001111001111001111001100001100001100"));
    assertEquals("55500011144466666699919777777", CodeDecode.decode("0011010011010011011010101111110011000011000011000011100011100011100011100011100011100001100100011001000110011100011001001111001111001111001111001111001111"));
    assertEquals("3331977777733322222211100019888", CodeDecode.decode("01110111011111000110010011110011110011110011110011110011110111011101110110011001100110011001101111111010101100011001000110000001100000011000"));
  }
}
