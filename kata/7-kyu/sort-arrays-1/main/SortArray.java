import java.util.Arrays;

class SortArray {
  static int[] sortArray(int[] names) {
    Arrays.sort(names);
    return names;
  }

  static long[] sortArray(long[] names) {
    Arrays.sort(names);
    return names;
  }

  static float[] sortArray(float[] names) {
    Arrays.sort(names);
    return names;
  }

  static double[] sortArray(double[] names) {
    Arrays.sort(names);
    return names;
  }

  static Object[] sortArray(Object[] names) {
    Arrays.sort(names);
    return names;
  }
}
